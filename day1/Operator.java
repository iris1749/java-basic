package day1;

public class Operator {
    public static void main(String[] args) {
        // 연산자

        // 산술연산자
        // +, -, *, /, %(나머지 몫)

        System.out.println(10 + 5);
        System.out.println(10 - 5);
        System.out.println(10 * 3);
        System.out.println(10 / 3);
        System.out.println(10 / 3.0);  // 정수와 정수의 계산 결과는 정수, 실수로 계산하려면 실수로 나누기를 해야함.
        System.out.println(10 % 3);

        // 비교연산자
        // >, <, >=, <=, ==, !=
        // true, false -> 논리값(boolean)을 출력함.

        System.out.println(10 > 5);
        System.out.println(10 < 5);
        System.out.println(10 >= 5);
        System.out.println(10 >= 5);
        System.out.println(5 == 5);   // 두개의 숫자가 같은지 여부를 확인
        System.out.println(10 != 5);  // 두개의 숫자가 다른지 여뷰를 확인

        // 논리 연산자


    }
}
